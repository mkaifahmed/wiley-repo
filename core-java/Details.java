public class Details {

    static void printdetails(String name, String city, String hobby) {
        System.out.println("Name : " + name + "\n" + "City : " + city + "\n" + "hobby : " + hobby);
    }

    public static void main(String[] args) {
        String name = "Kaif";
        String city = "Kurnool";
        String hobby = "Gamer";
        printdetails(name, city, hobby);
    }

}
